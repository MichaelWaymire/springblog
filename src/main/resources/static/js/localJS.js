function diceRoll() {
	var rollTheDice = Math.floor((Math.random() * 6) + 1);

	if(rollTheDice == 1){
		return "1";
	} else if ( rollTheDice == 2){
		return "2";
	}else if (rollTheDice == 3){
		return "3";
	}else if (rollTheDice == 4){
		return "4";
	}else if (rollTheDice == 5){
		return "5";
	}else{
		rollTheDice == 6
		return "7";
	}

}